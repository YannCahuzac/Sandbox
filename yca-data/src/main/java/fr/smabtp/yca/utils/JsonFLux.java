package fr.smabtp.yca.utils;

public class JsonFLux {

	public String lib1;
	public String lib2;

	public JsonFLux() {
		super();
	}

	public JsonFLux(String lib1, String lib2) {
		super();
		this.lib1 = lib1;
		this.lib2 = lib2;
	}

	public String getLib1() {
		return lib1;
	}

	public void setLib1(String lib1) {
		this.lib1 = lib1;
	}

	public String getLib2() {
		return lib2;
	}

	public void setLib2(String lib2) {
		this.lib2 = lib2;
	}

}